#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*p;
	int		i;

	i = 0;
	while (s[i])
		i++;
	p = (char *)malloc(sizeof(*p) * (i + 1));
	i = 0;
	while (s[i])
	{
		p[i] = s[i];
		i++;
	}
	return (p);
}
